public class Student {
    public String id;
    public String name;
    public String email;
    public String phone;
    public String course;

    public Student(String id, String name, String email, String phone, String course) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.course = course;
    }
}